package supply_chain1.model;

public class retail 
{
	private int retail_id;
	private String retail_name;
	private String retail_location;
	private String product_name;
	private int retail_quantity;
	private int retail_amount;
	private String retail_date;
	private String wholesaler_name;
	
	
	public int getRetail_id() 
	{
		return retail_id;
	}
	public void setRetail_id(int retail_id) 
	{
		this.retail_id = retail_id;
	}
	
	
	public String getRetail_name() 
	{
		return retail_name;
	}
	public void setRetail_name(String retail_name) 
	{
		this.retail_name = retail_name;
	}
	
	
	public String getRetail_location() 
	{
		return retail_location;
	}
	public void setRetail_location(String retail_location) 
	{
		this.retail_location = retail_location;
	}
	
	
	public String getProduct_name() 
	{
		return product_name;
	}
	public void setProduct_name(String product_name) 
	{
		this.product_name = product_name;
	}
	
	
	public int getRetail_quantity()
	{
		return retail_quantity;
	}
	public void setRetail_quantity(int retail_quantity) 
	{
		this.retail_quantity = retail_quantity;
	}
	
	
	public int getRetail_amount() 
	{
		return retail_amount;
	}
	public void setRetail_amount(int retail_amount)
	{
		this.retail_amount = retail_amount;
	}
	
	
	public String getRetail_date() 
	{
		return retail_date;
	}
	public void setRetail_date(String retail_date) 
	{
		this.retail_date = retail_date;
	}
	
	
	public String getWholesaler_name()
	{
		return wholesaler_name;
	}
	public void setWholesaler_name(String wholesaler_name) 
	{
		this.wholesaler_name = wholesaler_name;
	}

}
